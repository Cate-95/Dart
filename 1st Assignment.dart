void main() {
  // Example of integer data type
  int age = 25;

  // Example of double data type
  double weight = 65.5;

  // Example of string data type
  String name = "John Doe";

  // Example of list data type
  List<int> numbers = [1, 2, 3, 4, 5];

  // Example of map data type
  Map<String, dynamic> person = {
    'name': 'Alice',
    'age': 30,
    'isStudent': true,
  };

  // Printing out the values to demonstrate
  print('Age: $age'); // Output: Age: 25
  print('Weight: $weight'); // Output: Weight: 65.5
  print('Name: $name'); // Output: Name: John Doe
  print('Numbers: $numbers'); // Output: Numbers: [1, 2, 3, 4, 5]
  print('Person: $person'); // Output: Person: {name: Alice, age: 30, isStudent: true}
}
